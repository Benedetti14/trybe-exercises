let gradeP = 90;

if (gradeP>=90 && gradeP<=100){
  console.log("A");
}
else if(gradeP>=80){
  console.log('B');
}
else if(gradeP>=70){
  console.log('C');
}
else if(gradeP>=60){
  console.log('D');
}
else if(gradeP>=50){
  console.log('E');
}
else if(gradeP<50 && gradeP>=0){
  console.log('F');
}
else{
  console.log('Erro');
}